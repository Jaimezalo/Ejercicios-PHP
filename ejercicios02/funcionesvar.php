<?php
function sumar($num1, $num2){
    $suma = $num1 + $num2;
    return $suma;
}

function restar($num1, $num2){
    $resta = $num1 - $num2;
    return $resta;
}

function multiplicar($num1, $num2){
    $resta = $num1 * $num2;
    return $resta;
}

function dividir($num1, $num2){

    $division = $num1 / $num2;
    return $division;
}

function modulo($num1, $num2){
    $modulo = $num1 % $num2;
    return $modulo;
}

function potencia($num1, $num2){
    $resta = $num1 ** $num2;
    return $resta;
}
?>